'''
Data Types 
- A data type is a type of data that we can store inside a variable. 

There are four different types of data types in Python. 
- Strings
- Boolean
- Integers
- Floats
'''

# String
name = "Nano"  # str
print(type(name))

# Boolean
is_cool = True  # bool
print(type(is_cool))

# Integers
age = 20  # int
print(type(age))

# Floats
GPA = 4.0  # float
print(type(GPA))
